<script>
    $('.btn-pilih').click(function(e){
      e.preventDefault();
      const nama = $(this).data('nama')
      const id = $(this).data('id')
      const minimal = $(this).data('minimal')
              const point = '0'
      
      if(parseFloat(point) < minimal){
        toastr.error('Point minimal kamu kurang')
        return null
      } 

      $.ajax({
        method: 'GET',
        url: '/segarpoint/fetch/pilih',
        data: {
          id,
        },
        beforeSend: function(xhr){
          xhr.setRequestHeader('token', '');
          $("#pilihModalLabel").html(nama)
          $(".loading").fadeIn();
        },
        success: function(data){
          $("#pilihModal").modal('show')
          $(".modal-body-pilih").html(data);
          $(".loading").fadeOut();
        },
        error: function (request, status, error) {
          alert(request.responseText);
        }
      })
    })

    $(".form-beli").submit(function(){
      $(".loading").fadeIn()
    })
